function pickRandom(list) {
  return list[Math.floor(Math.random() * list.length)]
}

/** @returns {string} e.g. "Crimson Apple" */
export function randomDisplayName() {
  return `${pickRandom(COLORS)} ${pickRandom(FRUITS)}`
}
